package com.example.app_api;

public class ProgramLanguages {
    private String title, logo, desc, example, url;

    public ProgramLanguages(String title, String logo, String desc, String exmpl, String url) {
        this.title = this.title;
        this.logo = this.logo;
        this.desc = this.desc;
        this.example = example;
        this.url = this.url;
    }

    public String getTitle() {
        return title;
    }

    public String getLogo() {
        return logo;
    }

    public String getDesc() {
        return desc;
    }

    public String getExample() {
        return example;
    }

    public String getUrl() {
        return url;
    }
}
